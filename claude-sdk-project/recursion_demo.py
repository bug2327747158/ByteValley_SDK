def reverse_string(s):
    """
    用递归反转字符串
    
    递归思路：
    - "hello" 反转 = "o" + "hell" 的反转
    - 单字符 = 本身 (基准情况)
    """
    # 基准情况：空或单字符
    if len(s) <= 1:
        return s
    
    # 递归情况：最后一个字符 + 剩余部分的反转
    return s[-1] + reverse_string(s[:-1])

print("\n字符串反转演示：")
text = "Python"
print(f"  原字符串: {text}")
print(f"  反转结果: {reverse_string(text)}")


