def hanoi(n, source, target, auxiliary):
    """
    汉诺塔问题：将 n 个盘子从 A 柱移到 C 柱
    规则：一次只能移动一个，大盘子不能压在小盘子上
    
    递归思路：
    1. 把上面 n-1 个盘子从 A 移到 B（借助 C）
    2. 把第 n 个盘子从 A 移到 C
    3. 把 n-1 个盘子从 B 移到 C（借助 A）
    """
    # 基准情况：只有一个盘子，直接移动
    if n == 1:
        print(f"  移动盘子 1: {source} → {target}")
        return
    
    # 递归情况
    hanoi(n - 1, source, auxiliary, target)  # n-1 个移到辅助柱
    print(f"  移动盘子 {n}: {source} → {target}")  # 第 n 个移到目标柱
    hanoi(n - 1, auxiliary, target, source)  # n-1 个从辅助柱移到目标柱

print("\n3 个盘子的汉诺塔移动步骤 (A → C):")
hanoi(3, "A", "C", "B")


