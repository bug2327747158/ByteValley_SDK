def factorial_with_trace(n, depth=0):
    """带追踪的阶乘，展示递归调用过程"""
    indent = "  │" * depth + "  " if depth > 0 else ""
    print(f"{indent}┌─ factorial({n}) 被调用")
    
    if n <= 1:
        print(f"{indent}└─ 返回 1 (基准情况)")
        return 1
    
    result = n * factorial_with_trace(n - 1, depth + 1)
    print(f"{indent}└─ 返回 {result}")
    return result

print("\n计算 factorial(3) 的调用过程：")
print()
factorial_with_trace(3)
