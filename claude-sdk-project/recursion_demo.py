def sum_array(arr):
    """
    用递归求数组元素之和
    
    递归思路：
    - 数组之和 = 第一个元素 + 剩余元素之和
    - 空数组的和 = 0 (基准情况)
    """
    # 基准情况：空数组
    if len(arr) == 0:
        return 0
    
    # 递归情况：首元素 + 剩余元素的和
    return arr[0] + sum_array(arr[1:])

print("\n数组求和演示：")
arr = [1, 2, 3, 4, 5]
print(f"  数组: {arr}")
print(f"  求和过程: {arr[0]} + sum({arr[1:]})")
print(f"  结果: {sum_array(arr)}")


