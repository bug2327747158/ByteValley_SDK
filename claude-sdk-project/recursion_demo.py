"""
递归 (Recursion) - 函数调用自身的技术
========================

什么是递归？
-----------
递归是一种解决问题的方法，其中函数直接或间接地调用自身。

递归的两个核心要素：
1. 基准情况 (Base Case) - 终止条件，防止无限递归
2. 递归情况 (Recursive Case) - 将问题分解为更小的子问题

递归的思维方式：
-----------
"要解决这个问题，我需要先解决一个更小但结构相同的子问题"

类比理解：
-----------
想象你在找一本书：
- 你在第 N 层书架找
- 你不知道在哪，于是你对助手说：
  "你先在第 1 到 N-1 层找，找不到我再找第 N 层"
  
这就是递归 - 把大任务分解成相同结构的小任务
"""

# ==================== 示例 1: 阶乘 ====================
print("=" * 60)
print("示例 1: 阶乘")
print("=" * 60)

def factorial(n):
    """
    计算 n! = n × (n-1) × ... × 2 × 1
    
    递归思路：
    - 5! = 5 × 4!
    - 4! = 4 × 3!
    - ...
    - 1! = 1  (基准情况)
    """
    # 基准情况：终止递归的条件
    if n <= 1:
        return 1
    
    # 递归情况：调用自身处理更小的问题
    return n * factorial(n - 1)

print("\n计算阶乘：")
for i in range(1, 6):
    result = factorial(i)
    print(f"  {i}! = {result}")

print(f"\n  计算 5! 的过程：")
print(f"  factorial(5) = 5 × factorial(4)")
print(f"  factorial(4) = 4 × factorial(3)")
print(f"  factorial(3) = 3 × factorial(2)")
print(f"  factorial(2) = 2 × factorial(1)")
print(f"  factorial(1) = 1  ← 基准情况，开始返回")
print(f"  结果: 5 × 4 × 3 × 2 × 1 = {factorial(5)}")


