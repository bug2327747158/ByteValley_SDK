def fibonacci(n):
    """
    斐波那契数列: 1, 1, 2, 3, 5, 8, 13, 21, ...
    规则: F(n) = F(n-1) + F(n-2)
    
    递归思路：
    - 求第 n 个数，先求第 n-1 和 n-2 个数
    - 直到 n=1 或 n=2，返回 1
    """
    # 基准情况
    if n <= 2:
        return 1
    
    # 递归情况
    return fibonacci(n - 1) + fibonacci(n - 2)

print("\n斐波那契数列前 10 项：")
fib_sequence = [fibonacci(i) for i in range(1, 11)]
print(f"  {fib_sequence}")


